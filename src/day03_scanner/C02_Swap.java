package day03_scanner;

public class C02_Swap {
    public static void main(String[] args) {

        int sayi1 = 10, sayi2 = 20, sayi3 = 0;
        sayi3 = sayi2;
        sayi2 = sayi1;
        sayi1 = sayi3;

        System.out.println("Sayi1:" + sayi1);
        System.out.println("Sayi2:" + sayi2);


    }
}
